    const dispatch = document.querySelectorAll(".dpatch");
    const viewDetails = document.querySelectorAll(".itm-ck-det");
    const cardChild =document.querySelectorAll(".crd-chld");



    dispatch.forEach((elem)=>{
        elem.addEventListener("click",()=>{
            elem.style.backgroundColor ="black";
            elem.innerHTML = "Dispatched"
        })
    })
    viewDetails.forEach((element)=>{
        element.addEventListener("click",()=>{
            if(cardChild.classList !== "active"){
                element.parentElement.parentElement.parentElement.nextElementSibling.classList.toggle("active");
            }
        })
    })
</script>
